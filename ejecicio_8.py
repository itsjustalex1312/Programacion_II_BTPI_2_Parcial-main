# Pseudocodigo
# Solicitar la nota del usuario y guardarla en una variable “nota_usuario” 
# Si la variable “nota_usuario” es mayor a 70:
#	Imprimir por consola el texto “Has aprobado”
# Caso contrario:
#	Imprimir por consola el texto “No has aprobado”


# Codigo
nota_usuario = int(input("Ingrese su nota "))

if(nota_usuario > 80):
    print("Has aprobado ")
else:
    print("No has aprobado ")
